package week1.day2;

public class Calculator {

	public int addition() {

		int num1 = 10;
		int num2 = 20;
		int num3 = 40;

		int num = num1 + num2 + num3;

		return num;
	}

	public int subtraction() {

		int sub1 = 50;
		int sub2 = 30;

		int sumOfSub = sub1 - sub2;
		return sumOfSub;
	}

	public double multiplication() {

		double mul1 = 5.25;
		double mul2 = 10.20;

		double mul = mul1 * mul2;
		return mul;

	}

	public float division() {

		float div1 = 5.525f;
		float div2 = 6.66f;
		float div = div1 / div2;

		return div;

	}

}
